function readJson(filePath, fallback) {
  return fs.existsSync(filePath) ? JSON.parse(fs.readFileSync(filePath, "utf8")) : fallback;
}

function writeJson(filePath, value) {
  fs.writeFileSync(filePath, `${JSON.stringify(value, null, 2)}\n`);
}

function mergeProvider(providers, record) {
  const existingIndex = providers.findIndex((provider) => provider.id === record.id);
  if (existingIndex >= 0) {
    providers[existingIndex] = { ...providers[existingIndex], ...record };
    return "updated";
  }
  providers.push(record);
  return "added";
}

function addWatchItem(watchlist, item) {
  const index = watchlist.items.findIndex((existing) => existing.id === item.id);
  if (index >= 0) {
    watchlist.items[index] = { ...watchlist.items[index], ...item };
    return "updated";
  }
  watchlist.items.push(item);
  return "added";
}

